package com.lee.learn.dp.structualpattern.adapter.interfaceadapter;

public class PigList extends AbstractList {
    public PigList() {
        super();
    }

    @Override
    public int size() {
        return super.size();
    }

    @Override
    public boolean isEmpty() {
        return super.isEmpty();
    }

    @Override
    public void add(Object o) {
        super.add(o);
    }

    @Override
    public void remove(Object o) {
        super.remove(o);
    }

    @Override
    public void clear() {
        super.clear();
    }
}
